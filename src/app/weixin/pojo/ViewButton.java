package app.weixin.pojo;

/** 
 * view类型的菜单 
 *  
 * @author xsy 
 * @date 2013-05-13 
 */  
public class ViewButton extends Button {  
    private String type;  
    private String url;  
  
    public String getType() {  
        return type;  
    }  
  
    public void setType(String type) {  
        this.type = type;  
    }  
  
    public String getUrl() {  
        return url;  
    }  
  
    public void setUrl(String url) {  
        this.url = url;  
    }  
}  


